#!/usr/bin/env node
// Generate the Homebrew formula `wbhk.rb` for the webhook-co/homebrew-tap (DIST-10) from a release's
// version + checksums.txt. ONE source of truth: the initial tap commit AND the release auto-bump both run
// this, so the formula can't drift from the published binaries.
//
//   WBHK_FORMULA_VERSION=0.1.1 node gen-homebrew-formula.mjs <checksums.txt> [out.rb]
//
// checksums.txt is `<sha256>  <asset>` (sha256sum format — the release artifact). Homebrew is macOS + Linux
// only, so the windows asset is ignored. Emits to <out.rb> or stdout. `buildFormula`/`parseChecksums` are
// exported pure functions (unit-tested) — a wrong URL or swapped platform here ships a broken `brew install`.
import { readFileSync, writeFileSync } from "node:fs";
import { pathToFileURL } from "node:url";

const REPO = "webhook-co/webhook";

// asset name → the Homebrew platform block it belongs in (windows omitted — Homebrew is macOS + Linux).
export const FORMULA_PLATFORMS = [
  { asset: "wbhk-darwin-arm64", os: "macos", cpu: "arm" },
  { asset: "wbhk-darwin-x64", os: "macos", cpu: "intel" },
  { asset: "wbhk-linux-arm64", os: "linux", cpu: "arm" },
  { asset: "wbhk-linux-x64", os: "linux", cpu: "intel" },
];

/** Parse `<hex>  [*]<name>` lines (sha256sum format) → Map<name, lowercased-hex>. */
export function parseChecksums(text) {
  const sums = new Map();
  for (const line of text.split("\n")) {
    const m = /^([0-9a-fA-F]+)\s+\*?(.+?)\s*$/.exec(line.trim());
    if (m !== null) sums.set(m[2], m[1].toLowerCase());
  }
  return sums;
}

/** Build the `wbhk.rb` Homebrew formula string for `version`, pinning each platform asset to its sha256
 *  (from a Map<asset, sha>). Throws if a required platform asset is missing from the map. */
export function buildFormula(version, sums) {
  if (typeof version !== "string" || version.length === 0) {
    throw new Error("buildFormula: a version is required (e.g. 0.1.1)");
  }
  const block = ({ asset, cpu }) => {
    const sha = sums.get(asset);
    if (sha === undefined) throw new Error(`buildFormula: no checksum for ${asset}`);
    return `    on_${cpu} do
      url "https://github.com/${REPO}/releases/download/cli-v${version}/${asset}"
      sha256 "${sha}"
    end`;
  };
  const forOs = (os) =>
    FORMULA_PLATFORMS.filter((p) => p.os === os)
      .map(block)
      .join("\n");

  return `# typed: false
# frozen_string_literal: true

# Generated by packages/cli/scripts/gen-homebrew-formula.mjs from the cli-v${version} release — do not edit by
# hand; the release CD regenerates + pushes this on every tag.
class Wbhk < Formula
  desc "Capture, inspect, and replay webhooks from your terminal (webhook.co CLI)"
  homepage "https://webhook.co"
  version "${version}"
  license "Apache-2.0"

  on_macos do
${forOs("macos")}
  end

  on_linux do
${forOs("linux")}
  end

  def install
    # The release asset is a single bare binary (named per platform); install it as \`wbhk\`.
    bin.install Dir["wbhk-*"].first => "wbhk"
  end

  test do
    assert_match version.to_s, shell_output("#{bin}/wbhk --version")
  end
end
`;
}

// ── CLI (only when run directly, so the exports stay importable for tests) ──
if (process.argv[1] !== undefined && import.meta.url === pathToFileURL(process.argv[1]).href) {
  const version = process.env.WBHK_FORMULA_VERSION;
  if (version === undefined || version.length === 0) {
    console.error("gen-homebrew-formula: WBHK_FORMULA_VERSION is required (e.g. 0.1.1)");
    process.exit(1);
  }
  const checksumsPath = process.argv[2];
  if (checksumsPath === undefined) {
    console.error("gen-homebrew-formula: usage: gen-homebrew-formula.mjs <checksums.txt> [out.rb]");
    process.exit(1);
  }
  let formula;
  try {
    formula = buildFormula(version, parseChecksums(readFileSync(checksumsPath, "utf8")));
  } catch (err) {
    console.error(`gen-homebrew-formula: ${err instanceof Error ? err.message : String(err)}`);
    process.exit(1);
  }
  const out = process.argv[3];
  if (out !== undefined) {
    writeFileSync(out, formula);
    console.log(`✓ wbhk.rb (${version}) → ${out}`);
  } else {
    process.stdout.write(formula);
  }
}
